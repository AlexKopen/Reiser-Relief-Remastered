    <div class="flex-item row-2">
      <p>
        Bernard Reiser was born in 1924 to Aloys and Ottilia Reiser, farmers living in Wayzata, Minnesota. He was
        ordained to
        the priesthood on June 4, 1949 and was assigned to St. Mary of the Lake Parish in White Bear Lake, Minnesota. In
        1964,
        Archbishop Leo Binz asked Father Reiser to start a parish in the quickly-growing Minneapolis suburb of Coon
        Rapids.
        Seventy acres of farmland was acquired for the new church, which was named Epiphany after the great feast day of
        our
        Lord. Father Reiser's vision for the new parish was to see it become a home for people from conception to
        natural
        death.
      </p>
    </div>

