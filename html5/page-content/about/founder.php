    <div class="flex-item row-3">
      <p>
        Under Father Reiser's leadership, Epiphany grew to be one of the largest Catholic communities in Minnesota,
        totaling
        almost 20,000 people. In addition to the church, the Epiphany campus includes an elementary school, a Perpetual
        Adoration chapel, a senior housing complex, an assisted living facility, a cemetery, prayer garden, Stations of
        the
        Cross, and an outdoor shrine.
        <br><br>
        In 1996, Father Reiser traveled to Haiti for the first time. “I have seen slums in China, Egypt and Mexico
        City,” he
        wrote. “None of them compare to the tragic realities of the slums outside Port au Prince.” Father Reiser was
        profoundly impacted by his first experience in Haiti and resolved to spend the rest of his life raising
        awareness
        about the level of poverty in the country and alleviating the suffering of the poor. This was the beginning of
        Reiser
        Relief.
      </p>
    </div>

