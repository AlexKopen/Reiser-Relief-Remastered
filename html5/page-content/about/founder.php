    <div class="flex-item row-6">
      <p>
        Father Reiser returned to Haiti multiple times, where he started an elementary school, a home for the elderly,
        and a
        water truck ministry that provides clean water free of charge to residents of Cite Soleil, the poorest slum in
        the
        Western Hemisphere.
        <br><br>
        After 62 years of dedicated priesthood, Father Bernard Reiser went to the Lord on December 27, 2011 at the age
        of 86.
        Thousands of people mourned the passing of a beloved friend, mentor and pastor.
        <br><br>
        Reiser Relief carries on the legacy of Father Reiser by continuing the work he started in Haiti. Since our
        founding,
        the organization has grown to support four primary schools, three homes for the elderly, one home for disabled
        adults,
        one trade school, one water truck ministry, and one university scholarship program. We thank God for the life of
        Father Reiser, a visionary who made the world a better place.
      </p>
