  <h3>Education</h3>

  <div class="flex-container">
    <div class="flex-item container">
      <img src="https://reiserrelief.org/resources/images/about/work/education.jpg" alt="Education">
    </div>

    <div class="flex-item">
      <p>
        Education provides hope and opportunity for the future of Haiti. Because there are few public schools in the country,
        the only opportunity that most children have for education is through private schools. With an average monthly income
        of $50 USD, most families cannot afford to send their children to school.
        <br><br>
        We support four schools that together educate over 1,100 students. One of these schools is Terre Promise School
        located in Cite Soleil, a slum known to be the poorest and most dangerous area in the Western Hemisphere. Over 400
        students are enrolled in elementary through high school at Terre Promise.
        <br><br>
        In 2019, we began partnering with Sister Paesie to construct Saint Michael School in a different area of Cite Soleil.
        Sister Paesie was a Missionary of Charity sister for thirty years prior to founding a new community, The Kizito
        Family, named after a Ugandan martyr. There are now two other sisters and three postulants in The Kizito Family. We
        have worked with Sister Paesie to build a security wall, water tank, and latrines on her property in Cite Soleil. The
        school building is scheduled to be complete in 2021. Saint Michael School will be a haven for street children who
        otherwise would not have the opportunity to receive an education.
      </p>
    </div>

    <div class="flex-item row-2 container">
      <img src="https://reiserrelief.org/resources/images/about/work/agriculture.jpg" alt="Education">
    </div>

    <div class="flex-item row-1">
      <p>
        Reiser Heights School, located in the rural village of Donte, educates 314 students in kindergarten through ninth
        grade. We pay teacher salaries at Reiser Heights to ensure that school fees are affordable for poor families. We also
        sponsor a lunch program at Guardian Angels School in Leogane, where locally sourced food provides nutritious meals to
        over 250 students. Recently, we paid for the construction of a new cafeteria and sport court at Guardian Angels. This
        school is run by the Companions of Jesus sisters.
        <br><br>
        In 2015, Cecelia Duffy Memorial School was constructed in Marfranc, an isolated village in southwest Haiti. The Little
        Sisters of Saint Therese of the Child Jesus lovingly care for, and educate, 172 children in kindergarten through third
        grade. We are currently expanding Cecelia Duffy Memorial School by constructing a second building. This addition will
        provide space for fourth through sixth grade.
        <br><br>
        Our university program equips young men and women with the education and skills they need to provide for themselves,
        their future families, and become leaders within their communities. Our scholarship recipients are studying medicine,
        business, agriculture, and computer science at the best universities in Port au Prince.
      </p>
    </div>

