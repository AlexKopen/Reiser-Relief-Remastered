  <h3>Elder & Disabled Care</h3>

  <div class="flex-container">
    <div class="flex-item container">
      <img src="https://reiserrelief.org/resources/images/about/work/care.jpg" alt="Elder & Disabled Care">
    </div>

    <div class="flex-item">
      <p>
        A severe lack of public social services in Haiti leaves the elderly and disabled extremely vulnerable.
        <br><br>

        Fr. Bernard Reiser’s first project in Haiti was to construct Village of Jesus, a home for abandoned elderly women in
        Leogane. There are currently 40 women lovingly cared for here by Companions of Jesus sisters.
        <br><br>
        The same Haitian sisters manage another home for orphaned elderly women in Port au Prince. With your support, we
        renovated Sacred Heart Elderly Home. These renovations greatly improved overall accessibility and comfort for the
        residents. The remodeled kitchen allows large meals to be easily cooked by the staff, and a new outside patio gives
        elderly residents a safe place to enjoy the outdoors. We also constructed a security wall for the property and
        renovated the sister’s residence on the second floor of the building. Ongoing financial support provides locally
        sourced food and medical care for the 25 women at Sacred Heart.
      </p>
    </div>

    <div class="flex-item row-2 container">
      <img src="https://reiserrelief.org/resources/images/about/work/employment.jpg" alt="Elder & Disabled Care">
    </div>

    <div class="flex-item row-1">
      <p>
        In 2015, Reiser Relief began partnering with the Little Sisters of St. Therese of the Child Jesus in Marfranc. We
        rebuilt two eldercare facilities in the aftermath of the devastation caused by Hurricane Matthew. We continue to
        provide food and medical care to 32 elderly men and women cared for at these homes by the sisters.
        <br><br>
        Reiser Relief supports a home for disabled persons in Port au Prince, which is operated by religious brothers of the
        Missionaries of the Poor. We aide the missionaries in their care for severely disabled children and adults by
        assisting with facility improvements. These beloved children of God would have no hope for survival without the
        presence of the brothers who care for them with unconditional love.
      </p>
    </div>

